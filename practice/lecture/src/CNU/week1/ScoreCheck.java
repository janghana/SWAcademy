package CNU.week1;

public class ScoreCheck {
    public static void main(String[] args) {
        int score = 87;

        if (score >= 80) {
            System.out.println("B");
        } else if (score >= 85) {
            System.out.println("B+");
        } else {
            System.out.println("A");
        }
    }
}
