package CNU.week1;

public class Score {
    public static void main(String[] args) {
        int score = 87;

        if (score >= 90) {
            System.out.println("A");
        } else if (score >= 85) {
            System.out.println("B+");
        } else {
            System.out.println("B");
        }
    }
}
